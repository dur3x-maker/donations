export function getCampaignStory(campaign: CampaignListItem | CampaignDetail): StoryTone {
  const haystack = `${campaign.title} ${campaign.description}`.toLowerCase();
  const match = storyByTitle.find(([needle]) => haystack.includes(needle));
  if (match) return match[1];

  const index = Math.abs(
    campaign.id.split("").reduce((sum, char) => sum + char.charCodeAt(0), 0),
  ) % fallbackStories.length;
  return fallbackStories[index];
}
